package com.operators;

public class Arithemtic {

	public static void main(String[] args) {

		int a = 20;
		int b = 10;

		System.out.println("ARITHMETICC OPERATORS:");
		System.out.println("Addition: " + (a + b)); // Adds together two values
		System.out.println("Subtraction: " + (a - b)); // Subtracts one value from another
		System.out.println("Multiplication: " + (a * b)); // Multiplies two values
		System.out.println("Division: " + (a / b)); // Divides one value by another
		System.out.println("Modulus: " + (a % b)); // Returns the division remainder

	}

}
